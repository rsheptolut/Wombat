//+-----------------------------------------------------------------------------
//| Included files
//+-----------------------------------------------------------------------------
#include "ModelTexture.h"


//+-----------------------------------------------------------------------------
//| Constructor
//+-----------------------------------------------------------------------------
MODEL_TEXTURE::MODEL_TEXTURE()
{
	MaterialLayerNodes.SetData(this);
	ParticleEmitter2Nodes.SetData(this);
}


//+-----------------------------------------------------------------------------
//| Destructor
//+-----------------------------------------------------------------------------
MODEL_TEXTURE::~MODEL_TEXTURE()
{
	Clear();
}


//+-----------------------------------------------------------------------------
//| Clears the texture
//+-----------------------------------------------------------------------------
VOID MODEL_TEXTURE::Clear()
{
	TextureData = MODEL_TEXTURE_DATA();
}


//+-----------------------------------------------------------------------------
//| Returns the mdx size of the texture
//+-----------------------------------------------------------------------------
INT MODEL_TEXTURE::GetSize()
{
	return 268;
}


//+-----------------------------------------------------------------------------
//| Returns a reference to the data
//+-----------------------------------------------------------------------------
MODEL_TEXTURE_DATA& MODEL_TEXTURE::Data()
{
	return TextureData;
}
